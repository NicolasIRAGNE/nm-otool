#include "ft_printf.h"

void	ft_bzero(void *s, size_t n)
{
	while (n >= 8)
	{
		*(unsigned long *)(s) = 0;
		s += 8;
		n -= 8;
	}
	while (n > 0)
	{
		*(char *)(s) = 0;
		s += 1;
		n -= 1;
	}
}
